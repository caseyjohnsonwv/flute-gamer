import ctypes
import time

UP_ARROW = 0xC8
DOWN_ARROW = 0xD0
LIVE_KEYS = set()

class TrackmaniaHandler:
    def interpret_note(notestring:str) -> None:
        # notestring is expected to be "{note}{octave}", eg "A5"
        match notestring:
            case 'A4':
                key = DOWN_ARROW
                LIVE_KEYS.add(key)
                PressKey(key)
            case 'A5':
                key = UP_ARROW
                LIVE_KEYS.add(key)
                PressKey(key)
            case None:
                # release keys
                for key in LIVE_KEYS:
                    ReleaseKey(key)
                LIVE_KEYS.clear()




